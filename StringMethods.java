public class StringMethods {
    public static void main (String args []){
        String myFriends="aaaaaaa sdf ghg";
        System.out.println(myFriends.concat(" "+"pakistan"));
        System.out.println(myFriends.contains("pakistan"));
        System.out.println(myFriends.length());
        System.out.println(myFriends.charAt(9));
        System.out.println(myFriends.startsWith("aaa"));
        System.out.println(myFriends.endsWith("ghg"));

    }
}
